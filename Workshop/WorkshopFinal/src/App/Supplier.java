package App;

/**
 *
 * @author duclt
 */
public class Supplier {

    String code;
    String name;
    String address;
    boolean status;

    public Supplier() {

    }

    public Supplier(String code, String name, String address, boolean status) {
        this.code = code;
        this.name = name;
        this.address = address;
        this.status = status;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    @Override
    public String toString() {
        return code + " - " + name;
    }
}
