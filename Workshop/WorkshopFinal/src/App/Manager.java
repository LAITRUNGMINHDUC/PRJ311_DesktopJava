    private void tblSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSupplierMouseClicked
        // TODO add your handling code here:
        int pos = tblSupplier.getSelectedRow();
        if (pos >= 0) {
            Supplier sup = modelSupplier.getData().get(pos);
            this.txtSupCode.setText(sup.getCode());
            this.txtSupCode.setEditable(false);
            this.txtSupName.setText(sup.getName());
            this.txtAddress.setText(sup.getAddress());
            this.checkboxColla.setSelected(sup.isStatus());
            this.addNewSupplier = false;
        } else {
            Helper.btnNew(tblItems, txtItems);
        }
    }//GEN-LAST:event_tblSupplierMouseClicked

    private void tblItemsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblItemsMouseClicked
        // TODO add your handling code here:
        int pos = tblItems.getSelectedRow();
        if (pos >= 0) {
            Item item = modelItem.getData().get(pos);
            this.txtItemCode.setText(item.getCode());
            this.txtItemCode.setEditable(false);
            this.txtItemName.setText(item.getName());
            this.txtItemPrice.setText(item.getPrice() + "");
            this.txtItemUnit.setText(item.getUnit());
            this.listItemSupCode.setSelectedIndex(findPosInList(item.getSupCode()));

            this.checkboxSupply.setSelected(item.isStatus());
            this.addNewItem = false;
        } else {
            Helper.btnNew(tblItems, txtItems);
        }
    }//GEN-LAST:event_tblItemsMouseClicked

    private int findPosInList(String supCode) {
        for (int i = 0; i < listItemSupCode.getItemCount(); i++) {
            if (listItemSupCode.getItemAt(i).contains(supCode)) {
                return i;
            }
        }
        return -1;
    }

    private String findCodeInList(String row) {
        String[] S = row.split(" ");
        return S[0];
    }

