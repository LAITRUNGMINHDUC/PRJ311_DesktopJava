    private void btnSupSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupSaveActionPerformed
        // TODO add your handling code here:
        String code = txtSupCode.getText().trim();
        String name = txtSupName.getText().trim();
        String address = txtAddress.getText().trim();
        boolean status = checkboxColla.isSelected();
        Supplier sup = new Supplier(code, name, address, status);

        if (true) {
            if (addNewSupplier) {
                if (Helper.checkDuplicateCode(code, "SupCode", "Suppliers")) {
                    Helper.btnInsertUpdateSupplier(sup, "INSERT");
                    initDataSupplier();
                    JOptionPane.showMessageDialog(null, "Insert successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Duplicate Code");
                }
            } else {
                Helper.btnInsertUpdateSupplier(sup, "UPDATE");
                JOptionPane.showMessageDialog(null, "Update successfully");
                initDataSupplier();
            }
            initListSupplier();
        }
    }//GEN-LAST:event_btnSupSaveActionPerformed

