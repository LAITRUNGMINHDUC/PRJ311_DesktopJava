        String subCode = findCodeInList((String) listItemSupCode.getSelectedItem());
        String name = txtItemName.getText().trim();
        String unit = txtItemUnit.getText().trim();

        if (!txtItemPrice.getText().matches("[1-9]{1}[0-9]{1,8}")) {
            JOptionPane.showMessageDialog(null, "Error Price");
            return;
        }

        int price = Integer.parseInt(txtItemPrice.getText());
        boolean status = checkboxSupply.isSelected();
        Item item = new Item(code, name, subCode, unit, price, status);

        if (true) {
            if (addNewItem) {
                if (Helper.checkDuplicateCode(code, "itemCode", "items")) {
                    Helper.btnInsertUpdateItem(item, "INSERT");
                    JOptionPane.showMessageDialog(null, "Insert successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Duplicate code");
                }
            } else {
                Helper.btnInsertUpdateItem(item, "UPDATE");
                initDataSupplier();
                JOptionPane.showMessageDialog(null, "Update successfully");
            }
            initDataItem();
        }
