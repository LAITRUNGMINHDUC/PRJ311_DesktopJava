    void initDataSupplier() {
        modelSupplier = null;
        int[] indexes = {0, 1, 2};
        String[] header = {"Code", "Name", "Address"};
        modelSupplier = new SupplierModel(header, indexes);
        this.tblSupplier.setModel(modelSupplier);

        try (ResultSet rs = Helper.loadData("Suppliers")) {
            while (rs.next()) {
                String code = rs.getString("SupCode");
                String name = rs.getString("SupName");
                String address = rs.getString("Address");
                boolean status = rs.getBoolean("collaborating");
                modelSupplier.getData().add(new Supplier(code, name, address, status));
            }
            //rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    void initDataItem() {
        modelItem = null;
        int[] indexes = {0, 1, 3};
        String[] header = {"Code", "Name", "Price"};
        modelItem = new ItemModel(header, indexes);
        this.tblItems.setModel(modelItem);

        try {
            ResultSet rs = Helper.loadData("Items");
            while (rs.next()) {
                String code = rs.getString("itemCode");
                String name = rs.getString("itemName");
                String supCode = rs.getString("supCode");
                String unit = rs.getString("unit");
                int price = rs.getInt("price");
                boolean status = rs.getBoolean("supplying");
                modelItem.getData().add(new Item(code, name, supCode, unit, price, status));
            }
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    void initListSupplier() {
        this.listItemSupCode.removeAllItems();
        Vector<Supplier> data = modelSupplier.getData();
        for (Supplier x : data) {
            this.listItemSupCode.addItem(x.toString());
        }
    }

